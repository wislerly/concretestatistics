package com.nmz.concretestatistics.povo;



public class TypeOfShipping {
    public String ship_name;

    public double ship_unitprice;

    public double ship_mini;

    public double ship_miniprice;



    public String getShip_name() {
        return ship_name;
    }

    public void setShip_name(String ship_name) {
        this.ship_name = ship_name;
    }

    public double getShip_unitprice() {
        return ship_unitprice;
    }

    public void setShip_unitprice(double ship_unitprice) {
        this.ship_unitprice = ship_unitprice;
    }

    public double getShip_mini() {
        return ship_mini;
    }

    public void setShip_mini(double ship_mini) {
        this.ship_mini = ship_mini;
    }

    public double getShip_miniprice() {
        return ship_miniprice;
    }

    public void setShip_miniprice(double ship_miniprice) {
        this.ship_miniprice = ship_miniprice;
    }
}
